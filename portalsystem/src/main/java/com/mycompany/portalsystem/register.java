    private void regSignUpBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_regSignUpBtnActionPerformed
        try {
            // get the information from database
            String sql = "INSERT INTO student (student_number, first_name, middle_name, last_name, email, birth_date, gender, phone_number, address, religion, citizenship, civil_status, guardian, password) VALUES(?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?);";
            pst = con.prepareStatement(sql);
            pst.setString(1, regStudNum.getText());
            pst.setString(2, regFirstName.getText());
            pst.setString(3, regMidName.getText());
            pst.setString(4, regLastName.getText());
            pst.setString(5, regEmail.getText());
            
            String birthdate = regBirthday_month.getText() + "/" + regBirthday_day.getText() + "/" + regBirthday_year.getText();
            pst.setString(6, birthdate);
            
            String Gender = (String) regGender.getSelectedItem();
            pst.setString(7, Gender);
           
            pst.setString(8, regPhone.getText());
            pst.setString(9, regAddress.getText());
            pst.setString(10, regReligion.getText());
            pst.setString(11, regCitizenship.getText());
            
            String CivilStatus = (String) regCivilStatus.getSelectedItem();
            pst.setString(12, CivilStatus);
            
            pst.setString(13, regGuardian.getText());
            pst.setString(14, regPassword.getText());
            pst.execute();
            
            JOptionPane.showMessageDialog(bg, "You have successfully created an account. Please proceed to login.");
        }
        catch (SQLException ex) { System.out.println(ex.getMessage()); }
    }//GEN-LAST:event_regSignUpBtnActionPerformed

